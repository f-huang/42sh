#include "ft_42sh.h"

void	code_ctrl_u(void)
{
	int	i;

	i = 0;
	if (*copied() == NULL)
		return ;
	while ((*copied())[i])
	{
		ft_strinsert_input((*copied())[i]);
		(cor()->x)++;
		i++;
	}
}
