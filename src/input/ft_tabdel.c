#include "ft_42sh.h"

void	ft_tabdel(char ***tab)
{
	size_t	i;

	i = 0;
	if (!(*tab))
		return ;
	while ((*tab)[i])
	{
		free((*tab)[i]);
		i++;
	}
	if (*tab)
		free(*tab);
	*tab = NULL;
}
