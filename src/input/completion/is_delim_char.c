#include "tools.h"

int	is_delim_char(char *str, int i)
{
	if (!str)
		return (0);
	if (tl_iswhitespace(str[i]) ||\
		(str[i] == ';' && (i == 0 || str[i - 1] != '\\')) ||\
		(str[i] == '&' && ((i == 1 && str[i - 1] == '&') || \
		(i > 1 && str[i - 1] == '&' && str[i - 2] != '\\'))) ||\
		(str[i] == '|' && ((i == 1 && str[i - 1] == '|') ||\
		(i > 1 && str[i - 1] == '|' && str[i - 2] != '\\'))))
		return (1);
	return (0);
}
