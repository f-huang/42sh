#include "input.h"

int	ft_isvalid(char c)
{
	if (ft_isprint(c) || (c == '\x9') || c == '\x4' || c == '\x7f' || c == '\xa'
	|| c == '\x1b' || c == '\x5b' || c == '\x41' || c == '\x42' || c == '\x43'
	|| c == '\x44' || c == '\x48' || c == '\x46' || c == '\x31' || c == '\x32'
	|| c == '\x3b' || c == '\x42' || c == '\x41' || c == '\x18' || c == '\x15'
	|| c == '\x17' || c == '\x0c' || c == '\x01' || c == '\x05' || c == '\x12')
		return (1);
	return (0);
}
